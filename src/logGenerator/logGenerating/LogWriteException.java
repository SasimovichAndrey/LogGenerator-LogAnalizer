package logGenerator.logGenerating;

public class LogWriteException extends Exception {

	public LogWriteException() {
		super();
	}

	public LogWriteException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LogWriteException(String arg0) {
		super(arg0);
	}

	public LogWriteException(Throwable arg0) {
		super(arg0);
	}
}
